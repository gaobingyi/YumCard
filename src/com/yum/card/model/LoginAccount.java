package com.yum.card.model;

public class LoginAccount {
	String original_login_password;
	String original_login_type;
	String current_login_password;
	String current_login_type;
	
	public String getOriginal_login_password() {
		return original_login_password;
	}
	public void setOriginal_login_password(String original_login_password) {
		this.original_login_password = original_login_password;
	}
	public String getOriginal_login_type() {
		return original_login_type;
	}
	public void setOriginal_login_type(String original_login_type) {
		this.original_login_type = original_login_type;
	}
	public String getCurrent_login_password() {
		return current_login_password;
	}
	public void setCurrent_login_password(String current_login_password) {
		this.current_login_password = current_login_password;
	}
	public String getCurrent_login_type() {
		return current_login_type;
	}
	public void setCurrent_login_type(String current_login_type) {
		this.current_login_type = current_login_type;
	}
	
	
}	
	

